#!/usr/bin/env bash

set -euo pipefail

RED='\033[0;31m'
GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[0;33m'
NC='\033[0m'

info() {
    echo -e "${BLUE}❯❯${NC} $1"
}

success() {
    echo -e "${GREEN}❯❯${NC} $1"
}

warn() {
    echo -e "${YELLOW}❯❯${NC} $1"
}

fail() {
    echo -e "${RED}❯❯${NC} $1" >&2
    exit 1
}

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
ENV_SOURCE_FILE="$SCRIPT_DIR/.env"
ANTHROPIC_ENV_DIR="$HOME/.config/claude-code"
ANTHROPIC_ENV_FILE="$ANTHROPIC_ENV_DIR/anthropic.env"
ANTHROPIC_BASHRC_BLOCK_BEGIN="# BEGIN claude-code anthropic env"
ANTHROPIC_BASHRC_BLOCK_END="# END claude-code anthropic env"

if [[ "${EUID}" -eq 0 ]]; then
    fail "Run this as your normal user, not root."
fi

if ! command -v curl >/dev/null 2>&1; then
    fail "curl is required."
fi

info "Installing Claude Code"
curl -fsSL https://claude.ai/install.sh | bash

mkdir -p "$HOME/.local/bin"
mkdir -p "$ANTHROPIC_ENV_DIR"

PATH_LINE='export PATH="$HOME/.local/bin:$PATH"'
BASHRC="$HOME/.bashrc"

touch "$BASHRC"

if ! grep -Fqx "$PATH_LINE" "$BASHRC"; then
    {
        echo ""
        echo "# Added by install-claude-code.sh"
        echo "$PATH_LINE"
    } >> "$BASHRC"
    success "Added ~/.local/bin to ~/.bashrc"
else
    info "~/.bashrc already includes ~/.local/bin"
fi

write_anthropic_env_file() {
    local source_file="$1"
    local output_file="$2"
    local line key value trimmed_value
    local -a entries=()

    shopt -s extglob

    if [[ ! -f "$source_file" ]]; then
        warn "No .env file found at $source_file; skipping persistent Anthropic env export"
        return 0
    fi

    while IFS= read -r line || [[ -n "$line" ]]; do
        [[ "$line" =~ ^[[:space:]]*# ]] && continue
        [[ "$line" =~ ^[[:space:]]*$ ]] && continue

        if [[ "$line" =~ ^[[:space:]]*(export[[:space:]]+)?(ANTHROPIC[A-Z0-9_]*)=(.*)$ ]]; then
            key="${BASH_REMATCH[2]}"
            value="${BASH_REMATCH[3]}"
        else
            continue
        fi

        trimmed_value="$value"
        trimmed_value="${trimmed_value%$'\r'}"
        trimmed_value="${trimmed_value##+([[:space:]])}"
        trimmed_value="${trimmed_value%%+([[:space:]])}"

        if [[ "$trimmed_value" == \"*\" && "$trimmed_value" == *\" ]]; then
            trimmed_value="${trimmed_value:1:${#trimmed_value}-2}"
        elif [[ "$trimmed_value" == \'*\' && "$trimmed_value" == *\' ]]; then
            trimmed_value="${trimmed_value:1:${#trimmed_value}-2}"
        fi

        entries+=("$(printf 'export %s=%q' "$key" "$trimmed_value")")
    done < "$source_file"

    if [[ "${#entries[@]}" -eq 0 ]]; then
        warn "No ANTHROPIC* variables found in $source_file"
        return 0
    fi

    {
        echo "# Generated by install-claude-code.sh from $source_file"
        for line in "${entries[@]}"; do
            echo "$line"
        done
    } > "$output_file"

    chmod 600 "$output_file"
    success "Wrote persistent Anthropic env file to $output_file"
}

append_bashrc_block() {
    local bashrc_file="$1"
    local block_begin="$2"
    local block_end="$3"
    local source_file="$4"

    if grep -Fq "$block_begin" "$bashrc_file"; then
        info "~/.bashrc already sources the Anthropic env file"
        return 0
    fi

    {
        echo ""
        echo "$block_begin"
        echo "if [[ -f \"$source_file\" ]]; then"
        echo "    source \"$source_file\""
        echo "fi"
        echo "$block_end"
    } >> "$bashrc_file"
    success "Added Anthropic env loader to ~/.bashrc"
}

write_anthropic_env_file "$ENV_SOURCE_FILE" "$ANTHROPIC_ENV_FILE"
append_bashrc_block "$BASHRC" "$ANTHROPIC_BASHRC_BLOCK_BEGIN" "$ANTHROPIC_BASHRC_BLOCK_END" "$ANTHROPIC_ENV_FILE"

# Update the current script shell so follow-up checks see the new PATH.
# This does not change the parent shell; open a new terminal or source ~/.bashrc there.
# shellcheck disable=SC1090
source "$BASHRC"

if command -v claude >/dev/null 2>&1; then
    success "Claude Code installed at: $(command -v claude)"
else
    warn "Claude installed, but this shell still cannot find it."
    warn "Open a new terminal or run: source ~/.bashrc"
fi

success "Run 'claude doctor' to verify the installation."
